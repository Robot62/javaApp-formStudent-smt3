    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        String id = txtid.getSelectedItem().toString();

        try {
            String nama = txtnama.getText();
            String nim = txtnim.getText();
            String prodi = txtprodi.getText();

            pst = con.prepareStatement("update datamahasiswa set nim=?, nama=?, prodi=? where id=?");
            pst.setString(1,nim);
            pst.setString(2,nama);
            pst.setString(3,prodi);
            pst.setString(4,id);

            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate");
                txtnim.setText("");
                txtnama.setText("");
                txtprodi.setText("");
                txtnim.requestFocus();
                LoadMahasiswaID();
            }else{
                JOptionPane.showMessageDialog(this, "Data Gagal Diupdate");
            }

        } catch (SQLException ex) {
            Logger.getLogger(mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

