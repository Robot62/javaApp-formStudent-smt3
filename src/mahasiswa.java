    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String nama = txtnama.getText();
            String nim = txtnim.getText();
            String prodi = txtprodi.getText();

            pst = con.prepareStatement("insert into datamahasiswa(nim,nama,prodi) values (?,?,?)");
            pst.setString(1,nim);
            pst.setString(2,nama);
            pst.setString(3,prodi);

            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                txtnim.setText("");
                txtnama.setText("");
                txtprodi.setText("");
                txtnim.requestFocus();
                LoadMahasiswaID();
            }else{
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan");
            }

        } catch (SQLException ex) {
            Logger.getLogger(mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String id = txtid.getSelectedItem().toString();

            pst = con.prepareStatement("select * from datamahasiswa where id=? ");
            pst.setString(1,id);
            rs = pst.executeQuery();

            if(rs.next() == true){
                txtnim.setText(rs.getString(2));
                txtnama.setText(rs.getString(3));
                txtprodi.setText(rs.getString(4));
            }


        } catch (SQLException ex) {
            Logger.getLogger(mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

